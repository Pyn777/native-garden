const zones = document.querySelectorAll(".zone");
const map = document.getElementById("map");
const title = document.getElementById("bedTitle");
const plantList = document.getElementById("plantList");

let active = null;
let startX = 0;
let startY = 0;
let startLeft = 0;
let startTop = 0;
let moved = false;

async function getPlantImage(scientificName) {
  const cleanName = scientificName.replace(/[‘’']/g, "").split(" ").slice(0, 2).join("_");

  try {
    const response = await fetch(`https://en.wikipedia.org/api/rest_v1/page/summary/${cleanName}`);
    const data = await response.json();

    if (data.thumbnail && data.thumbnail.source) {
      return data.thumbnail.source;
    }
  } catch (error) {
    console.log("Image not found for", scientificName);
  }

  return "";
}

async function showBed(bed) {
  title.textContent = bed;
  plantList.innerHTML = "";

  zones.forEach(z => z.classList.remove("active"));

  zones.forEach(z => {
    if (z.dataset.bed === bed) z.classList.add("active");
  });

  for (const p of gardenData[bed]) {
    const li = document.createElement("li");
    li.innerHTML = `
      <strong>${p.name}</strong><br>
      <em>${p.scientific}</em><br>
      <span>Loading image...</span><br>
      Bloom: ${p.bloom}<br>
      Planted: ${p.planted}<br>
      Notes: ${p.notes}
      <br><br>
    `;
    plantList.appendChild(li);

    const imageUrl = await getPlantImage(p.scientific);

    if (imageUrl) {
      li.innerHTML = `
        <strong>${p.name}</strong><br>
        <em>${p.scientific}</em><br>
        <img src="${imageUrl}" alt="${p.name}" style="width:100%; max-width:220px; border-radius:10px; margin:8px 0;"><br>
        Bloom: ${p.bloom}<br>
        Planted: ${p.planted}<br>
        Notes: ${p.notes}
        <br><br>
      `;
    } else {
      li.innerHTML = `
        <strong>${p.name}</strong><br>
        <em>${p.scientific}</em><br>
        <span style="color:#777;">No image available</span><br>
        Bloom: ${p.bloom}<br>
        Planted: ${p.planted}<br>
        Notes: ${p.notes}
        <br><br>
      `;
    }
  }
}

zones.forEach(zone => {
  zone.addEventListener("pointerdown", e => {
    active = zone;
    moved = false;

    startX = e.clientX;
    startY = e.clientY;
    startLeft = zone.offsetLeft;
    startTop = zone.offsetTop;

    zone.setPointerCapture(e.pointerId);
  });

  zone.addEventListener("pointermove", e => {
    if (!active) return;

    const dx = e.clientX - startX;
    const dy = e.clientY - startY;

    if (Math.abs(dx) > 3 || Math.abs(dy) > 3) {
      moved = true;
    }

    let newLeft = startLeft + dx;
    let newTop = startTop + dy;

    const maxLeft = map.clientWidth - zone.offsetWidth;
    const maxTop = map.clientHeight - zone.offsetHeight;

    newLeft = Math.max(0, Math.min(maxLeft, newLeft));
    newTop = Math.max(0, Math.min(maxTop, newTop));

    zone.style.left = newLeft + "px";
    zone.style.top = newTop + "px";
  });

  zone.addEventListener("pointerup", () => {
    if (!active) return;

    savePositions();

    if (!moved) {
      showBed(active.dataset.bed);
    }

    active = null;
  });
});

function savePositions() {
  const data = {};
  zones.forEach(z => {
    data[z.id] = {
      left: z.offsetLeft,
      top: z.offsetTop
    };
  });
  localStorage.setItem("gardenLayout", JSON.stringify(data));
}

function loadPositions() {
  const saved = JSON.parse(localStorage.getItem("gardenLayout"));
  if (!saved) return;

  zones.forEach(z => {
    if (saved[z.id]) {
      z.style.left = saved[z.id].left + "px";
      z.style.top = saved[z.id].top + "px";
    }
  });
}

function resetPositions() {
  localStorage.removeItem("gardenLayout");
  location.reload();
}

document.getElementById("resetButton").onclick = resetPositions;

loadPositions();
showBed("Front Right");
